# operadores aritméticos:
print(22 *'~~')
v1 = float(input('digite o primeiro valor: '))
v2 = float(input('digite o segundo valor: '))
print(22 *'~~')
# Soma: 
soma = v1 + v2
print(f'A soma é = ({v1}) + ({v2}) =', soma)
sub = v1 - v2
print(f'A subtração é = ({v1}) - ({v2}) =', sub)
# multiplicação:
mult = v1 * v2
print(f'A multiplicação é = ({v1}) * ({v2}) =', mult)
# divisão:
div = v1 / v2
print(f'A divisão é = ({v1}) / ({v2}) =', div)
# Divisão Inteira:
div_int = v1 // v2
print(f'A divisão inteira é = ({v1}) // ({v2}) =', div_int)
# Resto da divisão:
resto = v1 % v2
print(f'O resto da divisão é = ({v1}) % ({v2}) =', resto)
# exponenciação:
exp = v1 ** v2
print(f'A exponenciação é = ({v1}) ** ({v2}) =', exp)
